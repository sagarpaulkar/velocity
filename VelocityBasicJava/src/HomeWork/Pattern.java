package HomeWork;

public class Pattern {
	public static void main(S[] args) {
		
		// Program 1 =To print square pattern contains *.
		
		for(int i=1; i<=4;i++) {
			for(int j=1;j<=4;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		System.out.println("==============================");
		
		// Program 2 =To print square pattern contains numbers  
		
		for(int k=1; k<=4;k++) {
		for(int l=1;l<=4;l++) {
			System.out.print(k);
		}
		System.out.println();
	}
		System.out.println("==============================");
		// Program 3 =To print square pattern contains numbers  
		for(int m=1; m<=4;m++) {
		for(int n=1;n<=4;n++) {
			System.out.print(n);
		}
		System.out.println();
	}
		System.out.println("==============================");
		// Program 4 =To print the  vertical reactangle
		for(int i=1; i<=8;i++) {
			for(int j=1;j<=5;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		System.out.println("==============================");
		// Program 5=To print the  horizontal reactangle
		for(int i=1; i<=5;i++) {
			for(int j=1;j<=8;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		System.out.println("==============================");
		// Program 6=print the triangular pattern.
		
		for(int i=1; i<=5; i++) {
			for(int j=1; j<=i;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		// Program 7=print the triangular pattern.
		System.out.println("==============================");
		
		for(int i=1; i<=5; i++) {
		for(int j=1; j<=i;j++) {
			System.out.print(j);
		}
		System.out.println();
	}
		System.out.println("==============================");
		// Program 8=print the triangular pattern.
		for(int i=1; i<=5; i++) {
		for(int j=1; j<=i;j++) {
			System.out.print(i);
		}
		System.out.println();
	}
		
				
	}

}